def set_paragraph_rtl(paragraph) -> None:
    p = paragraph._p
    p_pr = p.get_or_add_pPr()
    bidi = OxmlElement("w:bidi")
    bidi.set(qn("w:val"), "1")
    p_pr.append(bidi)
    paragraph.alignment = WD_ALIGN_PARAGRAPH.RIGHT

    for run in paragraph.runs:
        r_pr = run._r.get_or_add_rPr()
        rtl = OxmlElement("w:rtl")
        rtl.set(qn("w:val"), "1")
        r_pr.append(rtl)


def add_heading(doc: Document, text: str, level: int = 1) -> None:
    paragraph = doc.add_paragraph()
    run = paragraph.add_run(text)
    run.bold = True
    run.font.size = Pt(20 if level == 1 else 16 if level == 2 else 13 if level == 3 else 11)
    set_paragraph_rtl(paragraph)


def add_paragraph(doc: Document, text: str) -> None:
    paragraph = doc.add_paragraph(text)
    set_paragraph_rtl(paragraph)


def add_bullets(doc: Document, items: list[str]) -> None:
    for item in items:
        paragraph = doc.add_paragraph(style="List Bullet")
        paragraph.add_run(item)
        set_paragraph_rtl(paragraph)


def add_module(doc: Document, title: str, description: str, functional: list[str], non_functional: list[str]) -> None:
    add_heading(doc, title, 3)
    add_paragraph(doc, description)
    add_heading(doc, "דרישות פונקציונליות", 4)
    add_bullets(doc, functional)
    add_heading(doc, "דרישות לא פונקציונליות", 4)
    add_bullets(doc, non_functional)


